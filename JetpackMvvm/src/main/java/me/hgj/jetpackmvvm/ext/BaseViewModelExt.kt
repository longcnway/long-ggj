/**
 * 过滤服务器结果，失败抛异常
 * @param block 请求体方法，必须要用suspend关键字修饰
 * @param success 成功回调
 * @param error 失败回调 可不传
 * @param isShowDialog 是否显示加载框
 * @param loadingMessage 加载框提示内容
 */
fun <T> BaseViewModel.request(
    block: suspend () -> BaseHttpResultBean<T>,
    success: (T) -> Unit,
    error: (AppException) -> Unit = {},
    isShowDialog: Boolean = false,
    loadingMessage: String = "请求网络中..."
): Job {
    //如果需要弹窗 通知Activity/fragment弹窗
    return viewModelScope.launch {
        runCatching {
            if (isShowDialog) loadingChange.showDialog.postValue(loadingMessage)
            //请求体
            block()
        }.onSuccess {
            //网络请求成功 关闭弹窗
            loadingChange.dismissDialog.postValue(false)
            runCatching {
                //校验请求结果码是否正确，不正确会抛出异常走下面的onFailure
                executeResponse(it) { t -> success(t) }
            }.onFailure { e ->
                //打印错误消息
                e.message?.loge("JetpackMvvm")
                //失败回调
                error(ExceptionHandle.handleException(e))
                val responseThrowable: RetrofitException.ResponseThrowable =
                    RetrofitException.retrofitException(e)
                if(responseThrowable.code=="001"){
               // 跳转到登陆页面
                    ARouter.getInstance().build("/common/Logon")
                        .withTransition(
                            R.anim.slide_out_right,
                            R.anim.slide_in_left
                        )
                        .navigation()
                    AppActivityManager.getInstance().finishAllActivity()

                }
            }
        }.onFailure {
            //网络请求异常 关闭弹窗
            loadingChange.dismissDialog.postValue(false)
            //打印错误消息
            it.message?.loge("JetpackMvvm")
            //失败回调
            error(ExceptionHandle.handleException(it))
            val responseThrowable: RetrofitException.ResponseThrowable =
                RetrofitException.retrofitException(it)
            if(responseThrowable.code=="001"){
                // 跳转到登陆页面
                ARouter.getInstance().build("/common/Logon")
                    .withTransition(R.anim.slide_out_right, R.anim.slide_in_left)
                    .navigation()
                AppActivityManager.getInstance().finishAllActivity()

            }
        }
    }
}

